#  1. Convert: miles to kilometres and kilometres to miles.

# 1 mile = 1.61 kilometres

kilometres = 12.25
miles= 7.38

miles_to_kilometres = 7.38 * 1.61
kilometres_to_miles= 12.25/1.61

print(miles, "miles is", round(miles_to_kilometres, 2), "kilometres")
print(kilometres, "kilometres is", round(kilometres_to_miles, 2), "miles")


